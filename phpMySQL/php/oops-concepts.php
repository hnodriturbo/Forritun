Encapsulation:
// Encapsulation is the practice of bundling related data and functions into a 
// single unit called a class. It promotes data hiding and provides a way to 
// control access to class members.
 -->


<!-- 
Class:
// A class is a blueprint or template that defines the structure and behavior 
// of objects. It encapsulates data (properties) and functions (methods) that 
// operate on that data.
 -->


<!-- 
Object:
// An object is an instance of a class. It represents a specific entity with its 
// own unique state and behavior.
 -->


<!-- 
Inheritance:
// Inheritance is a mechanism that allows a class (child or derived class) to 
//inherit properties and methods from another class (parent or base class). 
// It promotes code reuse and supports the "is-a" relationship.
 -->


<!-- 
Polymorphism:
// Polymorphism allows objects of different classes to be treated as objects of a 
// common parent class. It enables the use of a single interface to represent different 
// types of objects.
 -->


<!-- 
Abstraction:
// Abstraction focuses on defining the essential features of an object while hiding the 
// implementation details. It allows for the creation of abstract classes and interfaces, 
// providing a blueprint for subclasses.
 -->


<!-- 
Interface:
// An interface defines a contract of methods that a class must implement. It specifies 
// the methods without defining their actual implementation. Classes can implement 
// multiple interfaces.
 -->



<!-- 
Constructor:
// A constructor is a special method that is automatically called when an object of a 
// class is created. It is used to initialize the object's properties or perform other 
// setup operations.
 -->


<!-- 
Destructor:
// A destructor is a special method that is automatically called when an object is 
// destroyed or goes out of scope. It is used to release resources or perform cleanup operations.
 -->

<!-- 
Access Modifiers:
// Access modifiers control the visibility and accessibility of class members 
// (properties and methods). The three common modifiers are public, private, 
// and protected, each providing different levels of access control.
